package test;



public class switch1 {
    public static void main(String[] args) {
        int a = 2;
        while (true) {
            switch (a) {
                case 1:
                    System.out.println("1111");
                    break;
                case 2:
                    System.out.println("2222");
                    break;
                default:
                    break;
            }
        }
    }
}
